/**
 * Decoration Data
 *
 * Defines all island decoration items that players can buy and place on tiles.
 * Each decoration occupies one grid cell (same as pets).
 */

export type DecorationCategory = 'trees' | 'flowers' | 'rocks' | 'water' | 'structures' | 'fun';

export interface DecorationDef {
  id: string;
  name: string;
  category: DecorationCategory;
  description: string;
  coinPrice: number;
  rarity: 'common' | 'uncommon' | 'rare' | 'epic';
  sprite: string;
  unlockLevel: number;
  /** Whether the decoration should have a gentle sway animation */
  sways: boolean;
  /** If set, this decoration is only available on the matching biome island.
   *  undefined = universal (available on all islands). */
  biome?: 'day' | 'beach' | 'winter' | 'desert' | 'night' | 'sakura';
}


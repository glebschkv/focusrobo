/** Lookup decoration by ID */
export function getDecorationById(id: string): DecorationDef | undefined {
  return DECORATIONS.find(d => d.id === id);
}

/** Get all decorations available at a given player level */
export function getUnlockedDecorations(playerLevel: number): DecorationDef[] {
  return DECORATIONS.filter(d => d.unlockLevel <= playerLevel);
}

/** Get decorations grouped by category */
export function getDecorationsByCategory(): Record<DecorationCategory, DecorationDef[]> {
  const grouped: Record<DecorationCategory, DecorationDef[]> = {
    trees: [],
    flowers: [],
    rocks: [],
    water: [],
    structures: [],
    fun: [],
  };
  for (const d of DECORATIONS) {
    grouped[d.category].push(d);
  }
  return grouped;
}

/** Biome ID to display name mapping */
export const BIOME_LABELS: Record<string, string> = {
  day: 'Meadow',
  beach: 'Beach',
  winter: 'Winter',
  desert: 'Desert',
  night: 'Night Garden',
  sakura: 'Sakura',
};

/** Get decorations available for a specific biome (universal + biome-specific) */
export function getDecorationsForBiome(biomeId: string): DecorationDef[] {
  return DECORATIONS.filter(d => !d.biome || d.biome === biomeId);
}

/** Get only biome-specific decorations for a given biome */
export function getBiomeDecorations(biomeId: string): DecorationDef[] {
  return DECORATIONS.filter(d => d.biome === biomeId);
}
